package org.hj.model;

public class logins {
    private String id;
    private String password;

    // 생성자, getter, setter 등 필요한 메서드 추가

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }
    
}
